    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String ecuacion = txtEcuacion.getText();
        char lista[] = txtEcuacion.getText().toCharArray();
        int n = lista.length - 1;

        StringBuffer buff = new StringBuffer(ecuacion);

        try {
            if (n > 1) {
                switch (lista[n]) {
                    case '(':
                        if (lista[n - 1] == 'p' || lista[n - 1] == 'g') {
                            buff.delete(n - 3, n + 1);
                        } else if (lista[n - 1] == 'n' || lista[n - 1] == 's') {
                            if (lista[n - 2] == 'l') {
                                buff.delete(n - 2, n + 1);
                            } else if ((n - 4) >= 0 && lista[n - 4] == 'a') {
                                buff.delete(n - 4, n + 1);
                            } else {
                                buff.delete(n - 3, n + 1);
                            }
                        } else {
                            buff.deleteCharAt(n);
                        }
                        break;
                    default:
                        buff.deleteCharAt(n);
                        break;
                }
            } else {
                buff.deleteCharAt(n);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

        txtEcuacion.setText(buff.toString());
    }//GEN-LAST:event_btnDeleteActionPerformed

